<template>
  <div class="store" v-if="isVisible">
    <div class="store__wrapper">
      <h2 class="store__title">{{ title }}</h2>
      <span class="store__location">{{ location }}</span>
      <img :src="photo" :alt="title" :title="title" />
    </div>
  </div>
</template>
<style lang="scss">
@import "./Store.scss";
</style>
<script>
export default {
  name: "Store",
  props: {
    title: {
      type: String,
      default: null,
      required: true,
    },
    location: {
      type: String,
      default: null,
      required: true,
    },
    photo: {
      type: String,
      default: null,
      required: true,
    },
  },
  computed: {
    isVisible() {
      return !!this.title && !!this.location;
    },
  },
};
</script>
